function [list,transmatrix] = evalueList (E,N,pr)
% E=20;
% N=100;
% pr=0.05;
z = 0:1:E;
x=0:1:E-1;
x_=E-1-x;
y=1:1:E;
y_=E+1-y;
k=E-z;
pp=1-pr;
R=k/E;
A=(pr/N)+(pp*k/E);
f=R.*(1-A);
g=R.*(1-A)+A.*(1-R)-1;
h=A.*(1-R);
A_=(pr/N)+(pp*x_/E);
R_=x_/E;
h_=A_.*(1-R_);
Rf=y_/E;
Af=(pr/N)+(pp*y_/E);
ff=Rf.*(1-Af);
transmatrix=(diag(-g)+diag(h_,1)+diag(ff,-1));
list=sort(eig(transmatrix));